import React from 'react';
import ReactDOM from 'react-dom';
 

class App extends React.Component {
  constructor() {
    super();
    this.state = {
      count: 0,
    };
  }
 
  render() {
    return (
      <button
        onClick={() => {
          this.setState({ count: this.state.count + 1 });
        }}
      >
        Count: {this.state.count}
      </button>
    );
  }
}

ReactDOM.render(<App />, document.getElementById('app'));
